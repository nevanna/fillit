#include "fillit.h"

void		ft_put_dot(int s, char ***m)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (j < s)
	{
		i = 0;
		while (i < s)
		{
			(*m)[j][i] = '.';
			i++;
		}
		j++;
	}
}
